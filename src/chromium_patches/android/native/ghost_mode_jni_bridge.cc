// Profile loading from SharedPreferences on startup
// Called from DeviceProfileStore constructor
// ====================================================================
bool LoadProfileFromJavaStorage(DeviceProfile* out_profile) {
  JNIEnv* env = base::android::AttachCurrentThread();
  if (!env) return false;

  // Get the application context
  auto context = base::android::GetApplicationContext();

  // Check if stored profile exists via SharedPreferences
  base::android::ScopedJavaLocalRef<jclass> clazz =
      base::android::GetClass(env, "org/nicegram/browser/GhostModeManager");
  jmethodID has_method = env->GetStaticMethodID(
      clazz.obj(), "hasStoredProfile", "(Landroid/content/Context;)Z");
  if (!has_method) return false;
  jboolean has = env->CallStaticBooleanMethod(clazz.obj(), has_method,
                                               context.obj());
  if (!has) return false;

  // Get stored JSON string
  jmethodID get_method = env->GetStaticMethodID(
      clazz.obj(), "getStoredProfileJson",
      "(Landroid/content/Context;)Ljava/lang/String;");
  if (!get_method) return false;
  base::android::ScopedJavaLocalRef<jstring> json_jstr(
      env,
      static_cast<jstring>(env->CallStaticObjectMethod(
          clazz.obj(), get_method, context.obj())));
  if (!json_jstr) return false;

  std::string json = base::android::ConvertJavaStringToUTF8(env, json_jstr);
  if (json.empty()) return false;

  // Parse JSON → DeviceProfile
  DeviceProfile parsed = DeviceProfile::FromJSON(json);
  if (parsed.manufacturer_name.empty()) return false;
  *out_profile = std::move(parsed);
  return true;
}

