// ====================================================================
// nativeClearHttpCache — Clear disk and memory HTTP cache + DNS cache
// ====================================================================
static void JNI_GhostModeManager_NativeClearHttpCache(
    JNIEnv* env) {
  content::BrowserContext* browser_context =
      ProfileManager::GetLastUsedProfile();
  if (!browser_context) return;

  auto* partition = browser_context->GetDefaultStoragePartition();
  if (!partition) return;

  // Clear HTTP cache
  partition->GetNetworkContext()->ClearHttpCache(
      base::Time(), base::Time::Max(),
      nullptr,  // filter
      base::DoNothing());

  // Clear DNS cache
  partition->GetNetworkContext()->ClearHostCache(
      nullptr,  // filter
      base::DoNothing());

  // Close idle sockets to prevent connection reuse tracking
  partition->GetNetworkContext()->CloseIdleConnections(
      base::DoNothing());

  // Clear HSTS state for all hosts
  partition->GetNetworkContext()->DeleteDynamicDataForHost(
      "*", base::DoNothing());
}

