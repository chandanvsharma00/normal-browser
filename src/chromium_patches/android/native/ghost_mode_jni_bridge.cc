// ====================================================================
// nativeNotifyProfileChanged — Notify all renderers of profile change
// ====================================================================
static void JNI_GhostModeManager_NativeNotifyProfileChanged(
    JNIEnv* env) {
  // Notify all active renderer processes to re-fetch the GhostProfile.
  // We iterate all RenderProcessHosts — each hosts one or more renderers
  // that cache the profile via GhostProfileClient.
  //
  // Each renderer's GhostProfileClient subscribed to the
  // GhostProfileProvider Mojo interface on startup. Sending a
  // re-initialization signal causes them to re-request the profile.
  //
  // Simplest correct approach: terminate and restart all renderer
  // processes. This forces re-initialization of GhostProfileClient
  // in each new renderer, picking up the latest profile.
  for (auto iter = content::RenderProcessHost::AllHostsIterator();
       !iter.IsAtEnd(); iter.Advance()) {
    content::RenderProcessHost* host = iter.GetCurrentValue();
    if (host && host->IsInitializedAndNotDead()) {
      // Fast shutdown triggers renderer restart on next navigation.
      // The renderer will re-fetch the profile via Mojo on startup.
      host->FastShutdownIfPossible(/* page_count= */ 0,
                                    /* skip_unload_handlers= */ true);
    }
  }
}

