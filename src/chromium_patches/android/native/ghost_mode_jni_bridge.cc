  content::BrowserContext* browser_context =
      ProfileManager::GetLastUsedProfile();
  if (!browser_context) return;

  auto* partition = browser_context->GetDefaultStoragePartition();
  if (!partition) return;

  // Delete all service workers and their caches
  partition->GetServiceWorkerContext()->DeleteAndStartOver(
      base::DoNothing());

  // Also clear Cache Storage (SW caches)
